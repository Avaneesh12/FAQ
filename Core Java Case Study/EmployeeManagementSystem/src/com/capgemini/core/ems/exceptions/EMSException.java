package com.capgemini.core.ems.exceptions;

public class EMSException extends Exception
{

	public EMSException() {
		super();
		
	}

	public EMSException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

	public EMSException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public EMSException(String arg0) {
		super(arg0);
		
	}

	public EMSException(Throwable arg0) {
		super(arg0);
		
	}

	
	
}
